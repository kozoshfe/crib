function createDefaultQuestions() {
  return defaultTestQuestions.map((question, index) => ({
    ...question,
    id: question.id || `default-${index}`,
    levels: question.levels?.length
      ? question.levels
      : Object.entries(levelQuestionIndexes)
        .filter(([, indexes]) => indexes.includes(index))
        .map(([level]) => level)
  }));
}
function normalizeQuestion(question, index) {
  return {
    id: question.id || `question-${Date.now()}-${index}`,
    question: String(question.question || ""),
    answers: Array.isArray(question.answers) ? question.answers.slice(0, 4) : ["", "", "", ""],
    correctIndex: Number.isInteger(question.correctIndex) ? question.correctIndex : 0,
    explanation: String(question.explanation || ""),
    levels: Array.isArray(question.levels) && question.levels.length ? question.levels : ["junior"]
  };
}
function loadTestQuestions() {
  try {
    const saved = JSON.parse(localStorage.getItem(TEST_QUESTIONS_KEY) || "null");
    if (Array.isArray(saved)) return saved.map(normalizeQuestion);
  } catch (error) {
    console.warn("Failed to load test questions", error);
  }
  return createDefaultQuestions();
}
