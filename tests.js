function buildAnswerFeedback(item, isCorrect) {
  const correctAnswer = item.answers[item.correctIndex] || "";
  const selectedAnswer = selectedIndex !== null ? item.answers[selectedIndex] || "" : "";

  if (isCorrect) {
    return `Правильно. Чому це правильна відповідь: ${item.explanation}`;
  }

  return `Неправильно. Твоя відповідь: ${selectedAnswer}. Правильна відповідь: ${correctAnswer}. Чому: ${item.explanation}`;
}

function renderAnsweredState() {
  const item = activeQuestions[currentIndex];
  if (!item || selectedIndex === null) return;

  const isCorrect = selectedIndex === item.correctIndex;
  answersList.querySelectorAll(".answer-option").forEach(button => {
    const index = Number(button.dataset.index);
    button.classList.toggle("correct", index === item.correctIndex);
    button.classList.toggle("wrong", index === selectedIndex && !isCorrect);
  });

  feedback.className = `feedback${isCorrect ? "" : " error"}`;
  feedback.textContent = buildAnswerFeedback(item, isCorrect);
  answerBtn.textContent = currentIndex === activeQuestions.length - 1 ? "Завершити" : "Наступне питання";
}

function restoreTestProgress() {
  try {
    const saved = JSON.parse(localStorage.getItem(TEST_PROGRESS_KEY) || "null");
    if (!saved || saved.questionsKey !== TEST_QUESTIONS_KEY || !Array.isArray(saved.questionIds)) return false;

    const restoredQuestions = saved.questionIds.map(findQuestionById);
    if (!restoredQuestions.length || restoredQuestions.some(question => !question)) return false;

    activeLevel = levelLabels[saved.level] ? saved.level : "junior";
    activeQuestions = restoredQuestions;
    currentIndex = Math.min(Math.max(Number(saved.currentIndex) || 0, 0), activeQuestions.length - 1);
    selectedIndex = Number.isInteger(saved.selectedIndex) ? saved.selectedIndex : null;
    answered = Boolean(saved.answered);
    correctCount = Math.max(Number(saved.correctCount) || 0, 0);

    renderLevelTabs();
    if (saved.completed) {
      renderResult(false);
    } else {
      renderQuestion({ keepState: true });
    }
    return true;
  } catch (error) {
    console.warn("Failed to restore test progress", error);
    return false;
  }
}

