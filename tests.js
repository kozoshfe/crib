function saveManagedQuestion(event) {
  event.preventDefault();
  const questionTextValue = testQuestionInput.value.trim();
  const answers = answerInputs.map(input => input.value.trim());
  const levels = getSelectedLevels();

  if (!questionTextValue || answers.some(answer => !answer) || !explanationInput.value.trim() || !levels.length) {
    alert("Заповни питання, 4 відповіді, пояснення і вибери хоча б один рівень.");
    return;
  }

  const question = {
    id: editingQuestionId || `question-${Date.now()}`,
    question: questionTextValue,
    answers,
    correctIndex: Number(correctAnswerSelect.value),
    explanation: explanationInput.value.trim(),
    levels
  };

  if (editingQuestionId) {
    testQuestions = testQuestions.map(item => item.id === editingQuestionId ? question : item);
  } else {
    testQuestions.push(question);
  }

  saveTestQuestions();
  clearTestProgress();
  resetTest(activeLevel, true);
  editManagedQuestion(question.id);
}
function deleteManagedQuestion() {
  if (!editingQuestionId) return;
  if (!confirm("Видалити це тестове питання?")) return;
  testQuestions = testQuestions.filter(question => question.id !== editingQuestionId);
  saveTestQuestions();
  clearTestProgress();
  resetTest(activeLevel, true);
  resetManagedQuestionForm();
}

function selectAnswer(index) {
  if (answered) return;
  selectedIndex = index;
  answerBtn.disabled = false;
  applySelectedAnswer();
  saveTestProgress(false);
}

function submitAnswer() {
  const item = activeQuestions[currentIndex];
  if (selectedIndex === null) return;

  if (answered) {
    currentIndex += 1;
    if (currentIndex >= activeQuestions.length) {
      renderResult();
    } else {
      renderQuestion();
      saveTestProgress(false);
    }
    return;
  }

  answered = true;
  const isCorrect = selectedIndex === item.correctIndex;
  if (isCorrect) correctCount += 1;

  correctCountEl.textContent = correctCount;
  answersList.querySelectorAll(".answer-option").forEach(button => {
    const index = Number(button.dataset.index);
    button.classList.toggle("correct", index === item.correctIndex);
    button.classList.toggle("wrong", index === selectedIndex && !isCorrect);
  });

  feedback.className = `feedback${isCorrect ? "" : " error"}`;
  feedback.textContent = buildAnswerFeedback(item, isCorrect);
  answerBtn.textContent = currentIndex === activeQuestions.length - 1 ? "Завершити" : "Наступне питання";
  saveTestProgress(false);
}

function renderResult(shouldSave = true) {
  const scorePercent = activeQuestions.length ? (correctCount / activeQuestions.length) * 100 : 0;
  questionProgress.textContent = "Тест завершено";
  progressFill.style.width = "100%";
  questionText.textContent = `Результат: ${correctCount} / ${activeQuestions.length}`;
  answersList.innerHTML = "";
  feedback.className = `feedback${scorePercent >= 90 ? "" : " error"}`;
  feedback.textContent = scorePercent >= 90
    ? "Ти готовий для цієї ролі"
    : "Потрібно ще вчитись";
  answerBtn.textContent = "Пройти ще раз";
  answerBtn.disabled = false;
  answered = true;
  if (shouldSave) saveTestProgress(true);
}

