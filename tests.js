function escapeHtml(text) {
  return String(text || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}
function resetTest(level = activeLevel, shouldShuffle = true, shouldSave = true) {
  activeLevel = level;
  const levelQuestions = buildLevelQuestions(activeLevel);
  activeQuestions = shouldShuffle ? shuffleList(levelQuestions) : levelQuestions;
  currentIndex = 0;
  selectedIndex = null;
  answered = false;
  correctCount = 0;
  renderLevelTabs();
  renderQuestion();
  if (shouldSave) saveTestProgress(false);
}
